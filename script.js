function getContactValidationMessage(key) {
  return CONTACT_VALIDATION_MESSAGES[currentLanguage]?.[key] || CONTACT_VALIDATION_MESSAGES.en[key];
}

function getContactFormStatus(form) {
  return form?.querySelector(".form-status, .modal-form-status") || null;
}

function getContactSubmitButton(form) {
  return form?.querySelector('button[type="submit"], input[type="submit"]') || null;
}

function resetContactFieldValidation(form) {
  form?.querySelectorAll("input, textarea").forEach((field) => {
    field.setCustomValidity("");
  });
}

function trimContactFormFields(form) {
  form?.querySelectorAll('input:not([type="hidden"]):not([type="submit"]):not([type="button"]), textarea').forEach((field) => {
    field.value = field.value.trim();
  });
}

function setContactSubmitState(form, isDisabled) {
  const submitButton = getContactSubmitButton(form);
  if (!submitButton) return;

  submitButton.disabled = isDisabled;

  if (isDisabled) {
    submitButton.setAttribute("aria-disabled", "true");
    form.dataset.isSubmitting = "true";
    return;
  }

  submitButton.removeAttribute("aria-disabled");
  delete form.dataset.isSubmitting;
}

function validateContactForm(form) {
  trimContactFormFields(form);
  resetContactFieldValidation(form);

  let firstInvalidField = null;

  CONTACT_REQUIRED_FIELD_NAMES.forEach((fieldName) => {
    const field = form.querySelector(`[name="${fieldName}"]`);
    if (!field) return;

    if (!field.value) {
      field.setCustomValidity(getContactValidationMessage("required"));
      firstInvalidField ||= field;
    }
  });

  const emailField = form.querySelector('[name="email"]');
  if (emailField && emailField.value && !CONTACT_EMAIL_PATTERN.test(emailField.value)) {
    emailField.setCustomValidity(getContactValidationMessage("email"));
    firstInvalidField ||= emailField;
  }

  if (!form.checkValidity()) {
    form.reportValidity();
    firstInvalidField?.focus();
    return false;
  }

  return true;
}

function initContactFormSubmission(form) {
  if (!form || form.dataset.contactFormReady === "true") return;

  form.dataset.contactFormReady = "true";
  form.setAttribute("action", CONTACT_FORM_ENDPOINT);
  form.setAttribute("method", "POST");

  const nextField = form.querySelector('input[name="_next"]');
  if (nextField) {
    nextField.value = CONTACT_FORM_SUCCESS_URL;
  }

  form.addEventListener("input", (event) => {
    const field = event.target;

    if (!(field instanceof HTMLInputElement || field instanceof HTMLTextAreaElement)) {
      return;
    }

    field.setCustomValidity("");
  });

  form.addEventListener("submit", (event) => {
    const status = getContactFormStatus(form);
    if (status) {
      status.textContent = "";
      status.classList.remove("success");
    }

    if (!validateContactForm(form)) {
      event.preventDefault();
      setContactSubmitState(form, false);
      return;
    }

    if (form.dataset.isSubmitting === "true") {
      event.preventDefault();
      return;
    }

    setContactSubmitState(form, true);

    let didNavigateAway = false;
    let unlockTimerId = 0;

    const handlePageHide = () => {
      didNavigateAway = true;
      cleanup();
    };

    const releaseOnFailure = () => {
      if (!didNavigateAway) {
        setContactSubmitState(form, false);
      }

      cleanup();
    };

    const cleanup = () => {
      window.removeEventListener("pagehide", handlePageHide);
      window.clearTimeout(unlockTimerId);
    };

    window.addEventListener("pagehide", handlePageHide, { once: true });
    unlockTimerId = window.setTimeout(releaseOnFailure, CONTACT_FORM_UNLOCK_DELAY);
  });

  window.addEventListener("pageshow", () => {
    setContactSubmitState(form, false);
  });
}

function createCatalogModalController({
  modalSelector,
  closeSelector,
  triggerSelector,
  onOpen,
  onClose
}) {
  const modal = document.querySelector(modalSelector);
  if (!modal) return null;

  const panel = modal.querySelector(".contact-modal-panel");
  const closeButton = modal.querySelector(closeSelector);
  const triggers = document.querySelectorAll(triggerSelector);
  let lastTrigger = null;

  const closeModal = () => {
    modal.classList.remove("is-open");
    modal.setAttribute("aria-hidden", "true");
    document.body.classList.remove("modal-open");
    onClose?.();
    lastTrigger?.focus();
    lastTrigger = null;
  };

  const openModal = (trigger) => {
    if (onOpen?.(trigger, modal) === false) {
      return;
    }

    lastTrigger = trigger;
    modal.classList.add("is-open");
    modal.setAttribute("aria-hidden", "false");
    document.body.classList.add("modal-open");
    setTimeout(() => closeButton?.focus(), 120);
  };

  triggers.forEach((trigger) => {
    trigger.addEventListener("click", () => {
      openModal(trigger);
    });

    trigger.addEventListener("keydown", (event) => {
      if (event.key === "Enter" || event.key === " ") {
        event.preventDefault();
        openModal(trigger);
      }
    });
  });

  closeButton?.addEventListener("click", closeModal);

  modal.addEventListener("click", (event) => {
    if (event.target === modal) {
      closeModal();
    }
  });

  panel?.addEventListener("click", (event) => {
    event.stopPropagation();
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape" && modal.classList.contains("is-open")) {
      closeModal();
    }
  });

  return {
    modal,
    closeModal,
    openModal,
    triggers
  };
}

